#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*r;
	int		i;

	r = malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!r)
		return (0);
	i = -1;
	while (s[++i])
		r[i] = s[i];
	r[i] = '\0';
	return (r);
}
